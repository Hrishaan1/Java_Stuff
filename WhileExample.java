public class WhileExample {
    public static void main(String[] args) {
        
        int n = 0;
        while (n < 3) {
            System.out.println((n+1) + ". Hrishaan is cool");
            n++;
        }

        System.out.println();
        

         for (int i = 0; i < 3; i++) {
             System.out.println((i + 1) + ". Bhargava is cool");
        }
    }
}
